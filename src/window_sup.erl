-module(window_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([init/1, start_link/1, start_child/5]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([]) ->
	
	ChildSpec = {sized_window_gen_server,
					{sized_window_gen_server, start_link, []}, 
				  	transient, 5000, worker, [sized_window_gen_server]},
	
	{ok, {{simple_one_for_one, 5, 1},[ChildSpec]}}.

%% ====================================================================
%% Internal functions
%% ====================================================================
start_link(FeedName) ->
	supervisor:start_link({local, FeedName}, ?MODULE, []).

%%TODO What if someone registers a name that is already registered?
start_child(FeedName, Name, RowQuery, ReduceQuery, QueryParameters) ->
	{ok,Pid} = supervisor:start_child(FeedName,[Name, RowQuery, ReduceQuery, QueryParameters]),
	
	ets:insert(window_ets, {Name, Pid}),
	
	Pid.